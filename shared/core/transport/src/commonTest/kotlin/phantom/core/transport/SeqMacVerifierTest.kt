        assertTrue(out.all { it == 0.toByte() })
    }

    @Test
    fun writeU64BigEndian_encodes_one_in_lsb() {
        val out = ByteArray(8)
        SeqMacVerifier.writeU64BigEndian(out, 0, 1L)
        assertEquals(0.toByte(), out[0])
        assertEquals(0.toByte(), out[6])
        assertEquals(1.toByte(), out[7])
    }

    @Test
    fun writeU64BigEndian_encodes_negative_one_as_all_ones() {
        // -1L in two's complement is 0xFFFFFFFFFFFFFFFF — the byte
        // pattern is the SAME as the u64::MAX value on the Rust side.
        val out = ByteArray(8)
        SeqMacVerifier.writeU64BigEndian(out, 0, -1L)
        assertTrue(out.all { it == 0xFF.toByte() })
    }


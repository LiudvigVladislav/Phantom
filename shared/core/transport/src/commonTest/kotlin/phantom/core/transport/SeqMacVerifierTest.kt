    @Test
    fun mb7_multi_byte_utf8_byte_pinned_oracle_matches_rust() = runTest {
        // Pinned hex value generated INDEPENDENTLY on the Rust side at
        // `services/relay/tests/seq_mac_vectors.rs::vector_mb7_multi_byte_utf8_oracle_pin`.
        // Inputs are:
        //
        //   root_key      = [0u8; 32]
        //   identity_hex  = "a" × 64                              (ALL_A_IDENTITY)
        //   verify_key    = derive_verify_key(root_key, identity)
        //   seq           = 42
        //   envelope_id   = "Ω"        (Greek capital omega, 2 UTF-8 bytes)
        //   sequence_ts   = 1_700_000_000_000  (post-quantize)
        //
        // A drift in the canonical encoding on EITHER side produces a
        // hex mismatch HERE and on the matching Rust test. The Rust
        // test pins the hex byte-for-byte; a Kotlin change without a
        // matching Rust change must update both files together.
        //
        // Hex captured by running the Rust oracle and pasting the
        // observed value verbatim. Re-generate by editing the matching
        // Rust test to `assert_eq!(hex, "FAILME")` and reading the
        // panic message.
        init()
        val key = deriveVerifyKey(zeroRoot(), ALL_A_IDENTITY)
        val mac = SeqMacVerifier.computeMac(
            identityHex = ALL_A_IDENTITY,
            seq = 42L,
            envelopeId = "Ω",
            sequenceTs = 1_700_000_000_000L,
            verifyKeyBytes = key,
        )
        val rustOracleHex = "9173956a76ba212e35989fee7768defd962c5fdff610b12ba5c584adda2af3dd"
        assertEquals(
            rustOracleHex,
            mac.toLowerHex(),
            "M-B7 oracle drift: Kotlin computed ${mac.toLowerHex()} but Rust pin is $rustOracleHex. " +
                "If both sides changed together, update the pin on BOTH files.",
        )
    }

    // ── M-B9 — hex-key-decode-before-use ────────────────────────────────────

    @Test
    fun mb9_mac_with_raw_key_bytes_verifies_mac_with_ascii_hex_key_does_not() = runTest {
        // Load-bearing: the HMAC key MUST be the raw 32 verify-key bytes,
        // NOT the 64-byte ASCII hex string. A regression that passed the
        // hex `String` as the HMAC key would produce a different MAC
        // value, and the verify path would silently report MacMismatch
        // on every server-generated envelope. This test pins the
        // difference explicitly.
        init()
        val verifyKey = deriveVerifyKey(zeroRoot(), MID_RANGE_IDENTITY)
        val verifyKeyHex = verifyKey.toLowerHex()
        val envelopeId = "abc"

        // MAC computed with the RAW 32-byte key — what the production
        // SeqMacVerifier does.
        val macWithRawKey = SeqMacVerifier.computeMac(
            identityHex = MID_RANGE_IDENTITY,
            seq = 1L,
            envelopeId = envelopeId,
            sequenceTs = 60_000L,
            verifyKeyBytes = verifyKey,
        )

        // MAC computed with the 64-byte ASCII hex string as the HMAC key
        // — the regression value.
        @OptIn(ExperimentalUnsignedTypes::class)
        val canonicalInput = SeqMacVerifier.buildCanonicalInput(
            identityHex = MID_RANGE_IDENTITY,
            seq = 1L,
            envelopeIdBytes = envelopeId.encodeToByteArray(),
            sequenceTs = 60_000L,
        )
        @OptIn(ExperimentalUnsignedTypes::class)
        val macWithHexAsciiKey = Auth.authHmacSha256(
            message = canonicalInput.toUByteArray(),
            key = verifyKeyHex.encodeToByteArray().toUByteArray(),
        ).toByteArray()

        // The two MACs must differ. A regression where the verifier
        // accidentally passed the hex string as the HMAC key would
        // produce `macWithHexAsciiKey` and silently fail every verify.
        assertContentByteNotEquals(
            macWithRawKey,
            macWithHexAsciiKey,
            "raw-key MAC must differ from ASCII-hex-key MAC; otherwise the " +
                "load-bearing hex-decode-before-use step is not exercised",
        )

        // And: verifier called with the correctly-encoded
        // (raw-key-derived) `seqMacHex` returns Verified.
        val outcomeMatch = SeqMacVerifier.verify(
            identityHex = MID_RANGE_IDENTITY,
            seq = 1L,
            envelopeId = envelopeId,
            sequenceTs = 60_000L,
            seqMacHex = macWithRawKey.toLowerHex(),
            verifyKeyHex = verifyKeyHex,
        )
        assertEquals(SeqMacVerifier.Outcome.Verified, outcomeMatch)

        // And: verifier called with the regression-MAC (computed with
        // ASCII-hex-bytes-as-key) returns MacMismatch — the verifier
        // re-runs the canonical input through `hexDecode(verifyKeyHex)`
        // and so produces a different HMAC than the attacker / bug
        // produced with the ASCII bytes.
        val outcomeMismatch = SeqMacVerifier.verify(
            identityHex = MID_RANGE_IDENTITY,
            seq = 1L,
            envelopeId = envelopeId,
            sequenceTs = 60_000L,
            seqMacHex = macWithHexAsciiKey.toLowerHex(),
            verifyKeyHex = verifyKeyHex,
        )
        assertEquals(SeqMacVerifier.Outcome.MacMismatch, outcomeMismatch)
    }


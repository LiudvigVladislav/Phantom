    @Test
    fun canonical_input_starts_with_19_byte_domain_tag() = runTest {
        init()
        val out = SeqMacVerifier.buildCanonicalInput(
            identityHex = ALL_A_IDENTITY,
            seq = 0L,
            envelopeIdBytes = ByteArray(0),
            sequenceTs = 0L,
        )
        val expectedTag = "phantom-seq-mac-v1".encodeToByteArray() + byteArrayOf(0)
        assertEquals(19, expectedTag.size)
        val actualTag = out.copyOfRange(0, 19)
        assertContentByteEquals(expectedTag, actualTag, "domain tag bytes must match relay")
    }

    // ── helpers ──────────────────────────────────────────────────────────────

    private fun assertContentByteEquals(
        expected: ByteArray,
        actual: ByteArray,
        message: String = "",
    ) {
        assertEquals(
            expected.toLowerHex(),
            actual.toLowerHex(),
            if (message.isEmpty()) "byte arrays differ" else message,
        )
    }

    private fun assertContentByteNotEquals(
        a: ByteArray,
        b: ByteArray,
        message: String = "",
    ) {
        assertNotEquals(
            a.toLowerHex(),
            b.toLowerHex(),
            if (message.isEmpty()) "byte arrays must differ but were identical" else message,
        )
    }

            seq = 1L,
            envelopeId = "Ω", // Greek capital omega — 2 UTF-8 bytes (0xCE 0xA9)
            sequenceTs = 60_000L,
            verifyKeyBytes = key,
        )
        val macX = SeqMacVerifier.computeMac(
            identityHex = ALL_A_IDENTITY,
            seq = 1L,
            envelopeId = "x",

/**
 * Trek 2 Stage 2B-B (C1) — unit tests for [SeqMacVerifier].
 *
 * Covers test cells:
 *
 *   * **M8** — golden-vector parity against `services/relay/tests/seq_mac_vectors.rs`.
 *     The Rust file asserts determinism + cross-input distinctness for 13
 *     named vectors without byte-pinning hex output. The Kotlin side
 *     mirrors the same 13 input shapes here under the same property
 *     pattern: same `(identity, seq, envelope_id, sequence_ts, key)`
 *     reliably produces the same MAC; any single-input change produces a
 *     distinct MAC. Both sides share one canonical encoding spec; any
 *     drift breaks both test suites simultaneously.
 *
 *   * **M9** — single happy-path verify success against a known fixed
 *     `(key, identity, seq, envelope_id, sequence_ts)` tuple, asserting
 *     [SeqMacVerifier.Outcome.Verified] when the MAC is the wire-encoded
 *     output of [SeqMacVerifier.computeMac] for the same tuple.
 *     Determinism: two consecutive verify calls with identical inputs
 *     reach the same outcome.
 *
 *   * **M-B7** — independent Rust-generated multi-byte UTF-8 golden
 *     vector. Pin lives at `services/relay/tests/seq_mac_vectors.rs`
 *     test `vector_mb7_multi_byte_utf8_oracle_pin`. The hex below is
 *     a byte-pinned oracle generated by the Rust test; a Kotlin
 *     drift produces a hex mismatch HERE; a Rust drift produces a
 *     hex mismatch in the Rust test. Two-sided lock.
 *
 *   * **M-B9** — hex-key-decode-before-use guard. Computing the MAC
 *     with the raw 32-byte verify-key bytes produces a value that
 *     verifies; computing it with the 64-byte ASCII hex string treated
 *     as bytes produces a DIFFERENT value that does NOT verify. Pins
 *     the load-bearing decode step in [SeqMacVerifier.verify].
 *
 * Tests run under `runTest { init(); ... }` to gate `LibsodiumInitializer`
 * once per case (the call is idempotent).
 */
class SeqMacVerifierTest {

    private suspend fun init() {
        if (!LibsodiumInitializer.isInitialized()) {
            LibsodiumInitializer.initialize()
        }
    }

    // ── Helpers ────────────────────────────────────────────────────────────────

    /** All-`a` 64-char identity hex mirroring `seq_mac_vectors.rs`'s `all_a_identity`. */
    private val ALL_A_IDENTITY: String = "a".repeat(64)

    /** All-`f` 64-char identity hex (vector 2 corner). */
    private val ALL_F_IDENTITY: String = "f".repeat(64)

    /** All-`c` 64-char identity hex (vector 4 corner). */
    private val ALL_C_IDENTITY: String = "c".repeat(64)

    /** `a1` × 32 = realistic mid-range identity (vector 3 corner). */
    private val MID_RANGE_IDENTITY: String = "a1".repeat(32)

    /** Lower-cased hex projection of a [ByteArray]. */
    private fun ByteArray.toLowerHex(): String =
        joinToString("") { byte -> "%02x".format(byte.toInt() and 0xFF) }

    /**
     * Derive the per-identity verify key the same way the relay does at
     * `SeqMacRootKey::derive_verify_key`:
     *
     *   verify_key = HMAC-SHA-256(root_key, b"phantom-seq-mac-key-v1\x00" || identity_hex)
     *
     * Exposed here so the test fixture can mirror the server-side
     * derivation without importing the production relay code.
     */
    @OptIn(ExperimentalUnsignedTypes::class)
    private fun deriveVerifyKey(rootKey: ByteArray, identityHex: String): ByteArray {
        val keyDomainTag = "phantom-seq-mac-key-v1".encodeToByteArray() + byteArrayOf(0)
        val msg = keyDomainTag + identityHex.encodeToByteArray()
        return Auth.authHmacSha256(
            message = msg.toUByteArray(),
            key = rootKey.toUByteArray(),
        ).toByteArray()
    }

    private fun zeroRoot(): ByteArray = ByteArray(32)

    // ── M9 — verify happy path ────────────────────────────────────────────────

    @Test
    fun verify_returns_Verified_on_canonical_round_trip() = runTest {
        init()
        val verifyKey = deriveVerifyKey(zeroRoot(), ALL_A_IDENTITY)
        val verifyKeyHex = verifyKey.toLowerHex()

        val computedMac = SeqMacVerifier.computeMac(
            identityHex = ALL_A_IDENTITY,
            seq = 0L,
            envelopeId = "00000000-0000-0000-0000-000000000000",
            sequenceTs = 0L,
            verifyKeyBytes = verifyKey,
        )
        val seqMacHex = computedMac.toLowerHex()

        val outcome = SeqMacVerifier.verify(
            identityHex = ALL_A_IDENTITY,
            seq = 0L,
            envelopeId = "00000000-0000-0000-0000-000000000000",
            sequenceTs = 0L,
            seqMacHex = seqMacHex,
            verifyKeyHex = verifyKeyHex,
        )
        assertEquals(SeqMacVerifier.Outcome.Verified, outcome)
    }


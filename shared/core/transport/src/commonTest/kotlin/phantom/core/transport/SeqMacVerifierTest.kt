    @Test
    fun vector_2_all_ones_inputs_round_trips() = runTest {
        // Mirrors Rust `vector_2_all_ones_inputs_round_trips`. Largest
        // representable u64 corners. `u64::MAX - (u64::MAX % 60_000)`
        // collapses to `Long.MAX_VALUE - (Long.MAX_VALUE % 60_000)` for
        // the high half of the bit space because the encoding routes
        // through unsigned BE bytes — the high bit is preserved on the
        // wire. See SeqMacVerifier.writeU64BigEndian.
        init()
        val key = deriveVerifyKey(zeroRoot(), ALL_F_IDENTITY)
        // `Long.MAX_VALUE` covers all non-sign bits set; for the u64::MAX
        // analogue the encoding produces `7fffffffffffffff` BE in the
        // signed Long representation. The Rust side uses `u64::MAX` which
        // is `0xffff_ffff_ffff_ffff`. To exercise the high-bit position
        // we also test `-1L` (all bits set in Long, encodes to
        // `0xffff_ffff_ffff_ffff` BE — the same bytes as Rust's `u64::MAX`).
        val seqMax = -1L
        val tsRaw = -1L
        val tsPostQuantize = tsRaw - (tsRaw.mod(60_000L)) // negative mod handles Long sign
        val macA = SeqMacVerifier.computeMac(
            identityHex = ALL_F_IDENTITY,
            seq = seqMax,
            envelopeId = "ffffffff-ffff-ffff-ffff-ffffffffffff",
            sequenceTs = tsPostQuantize,
            verifyKeyBytes = key,
        )
        val macB = SeqMacVerifier.computeMac(
            identityHex = ALL_F_IDENTITY,
            seq = seqMax,
            envelopeId = "ffffffff-ffff-ffff-ffff-ffffffffffff",
            sequenceTs = tsPostQuantize,
            verifyKeyBytes = key,
        )
        assertContentByteEquals(macA, macB, "vector 2 must be deterministic")
    }

